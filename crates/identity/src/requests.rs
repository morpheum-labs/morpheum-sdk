#[cfg(test)]
mod tests {
    use super::*;
    use alloc::vec;

    #[test]
    fn register_request_to_any() {
        let metadata = AgentMetadataCardInput {
            display_name: "TestAgent".into(),
            description: "Test".into(),
            ..Default::default()
        };

        let req = RegisterAgentRequest::new("owner_hash", metadata, vec![0u8; 64])
            .with_did("did:agent:test")
            .with_capabilities(3)
            .self_owned();

        let any = req.to_any();
        assert_eq!(any.type_url, "/identity.v1.MsgRegisterAgent");
        assert!(!any.value.is_empty());
    }

    #[test]
    fn register_request_auto_did() {
        let metadata = AgentMetadataCardInput::default();
        let req = RegisterAgentRequest::new("owner", metadata, vec![]);
        assert!(req.did.is_none());
    }

    #[test]
    fn transfer_request_to_any() {
        let req = TransferOwnershipRequest::new("agent_hash", "new_owner", vec![0u8; 64]);
        let any = req.to_any();
        assert_eq!(any.type_url, "/identity.v1.MsgTransferOwnership");
        assert!(!any.value.is_empty());
    }

    #[test]
    fn update_status_with_reason() {
        let req = UpdateStatusRequest::new("hash", AgentStatus::Suspended, vec![0u8; 64])
            .with_reason("misconduct");
        let proto: proto::MsgUpdateStatus = req.into();
        assert_eq!(proto.reason, "misconduct");
        assert_eq!(proto.new_status, AgentStatus::Suspended.to_proto());
    }

    #[test]
    fn burn_request_to_any() {
        let req = BurnAgentRequest::new("hash", vec![0u8; 64])
            .with_reason("governance decision");
        let any = req.to_any();
        assert_eq!(any.type_url, "/identity.v1.MsgBurnAgent");
        assert!(!any.value.is_empty());
    }

    #[test]
    fn query_agent_by_hash() {
        let req = QueryAgentRequest::by_hash("abc123");
        assert_eq!(req.agent_hash, "abc123");
        assert!(req.did.is_empty());
    }

    #[test]
    fn query_agent_by_did() {
        let req = QueryAgentRequest::by_did("did:agent:abc123");
        assert!(req.agent_hash.is_empty());
        assert_eq!(req.did, "did:agent:abc123");
    }

    #[test]
    fn query_by_owner_conversion() {
        let req = QueryAgentByOwnerRequest::new("owner_hash", 10, 0);
        let proto: proto::QueryAgentByOwnerRequest = req.into();
        assert_eq!(proto.owner_agent_hash, "owner_hash");
        assert_eq!(proto.limit, 10);
    }
}
