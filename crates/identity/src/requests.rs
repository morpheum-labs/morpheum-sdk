/// Query an agent identity by hash or DID.
///
/// Use the named constructors `by_hash()` or `by_did()` for clarity.
#[derive(Clone, Debug, PartialEq, Eq)]
#[cfg_attr(feature = "serde", derive(Serialize, Deserialize))]
pub struct QueryAgentRequest {
    pub agent_hash: String,
    pub did: String,
}

impl QueryAgentRequest {
    /// Lookup by agent hash.
    pub fn by_hash(agent_hash: impl Into<String>) -> Self {
        Self {
            agent_hash: agent_hash.into(),
            did: String::new(),
        }
    }

    /// Lookup by DID string.
    pub fn by_did(did: impl Into<String>) -> Self {
        Self {
            agent_hash: String::new(),
            did: did.into(),
        }
    }
}

impl From<QueryAgentRequest> for proto::QueryAgentRequest {
    fn from(req: QueryAgentRequest) -> Self {
        Self {
            agent_hash: req.agent_hash,
            did: req.did,
        }
    }
}

